    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        // TODO add your handling code here:
        Random r = new Random();
        int low = (int) this.spInferior.getValue();
        int high = (int) this.spSuperior.getValue();
        int result = r.nextInt(high-low) + low;
        this.tfNumero.setText(Integer.toString(result));
    }//GEN-LAST:event_btnGenerarActionPerformed

    private void spInferiorStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_spInferiorStateChanged
        // TODO add your handling code here:
        int low = (int) this.spInferior.getValue();
        int high = (int) this.spSuperior.getValue();   
        
        if (low>=high){
            JOptionPane.showMessageDialog(this, "El valor inferior es mayor que el superior", "Advertencia", JOptionPane.WARNING_MESSAGE);
            this.spInferior.setValue(high-1);
        }
    }//GEN-LAST:event_spInferiorStateChanged

    private void spSuperiorStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_spSuperiorStateChanged
        // TODO add your handling code here:
        int low = (int) this.spInferior.getValue();
        int high = (int) this.spSuperior.getValue();   
        
        if (high<=low){
            JOptionPane.showMessageDialog(this, "El valor superior debe ser mayor que el inferior", "Advertencia", JOptionPane.WARNING_MESSAGE);
            this.spSuperior.setValue(low+1);
        }
    }//GEN-LAST:event_spSuperiorStateChanged

